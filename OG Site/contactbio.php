            <h3>Biography</h3>
            Ralph Mindicino was born in 1960 on Long Island, New York. He studied fine art at the State University of New York at Fredonia and Stonybrook, acquiring skills in bronze and steel sculpture, pottery and oil painting.  Ralph Mindicino sacrificed his right leg to bone cancer at the age of 14. The prospect of a long recovery mired in pain and boredom had a transformational effect on his psyche. Creating imaginary worlds provided escape and relief from the harsh realities of the physical world. My rehabilitation and psychic healing concluded with an outlook on life forever informed by the experience. 
            <br /><br />
            Since 2005 Mr. Mindicino has been working exclusively with acrylic Plexi glass, producing delicate hand engravings with a vibrant palette to create organically inspired abstractions and cityscapes. 
            <br /><br />
            Winner of the prestigious Wynn Newhouse Award in 2009, Ralph Mindicino&rsquo;s paintings have been displayed at the Knoedler Gallery, Delaware Art Museum and numerous national juried exhibitions. Recent highlights include &lsquo;Wounded In Action&rsquo; Art Exhibition which toured nationally until 2012 with locations including the Senate Rotunda in Washington DC and the National Museum at Walter Reed. In 2012, the Rome Art Center hosted a solo show of Mr. Mindicino&rsquo;s work. Additionally, several pieces were selected to become part of the permanent art collections of the Spaulding Rehabilitation Hospital in Charlestown MA and Bryn Mawr Rehabilitation Hospital in Malvern PA.  Several pieces can also be seen as part of the set design for CBS&rsquo;s &ldquo;Person of Interest&rdquo; & ABC&rsquo;s &ldquo;Body of Proof&rdquo;.
