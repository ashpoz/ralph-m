        	<h3>Artist Statement</h3>
            The most significant shift to the landscape of my life certainly resulted from having cancer at a young age. Not only was my survival in question; a fact that is difficult at any age to comprehend, but my right leg was also amputated at the hip.  I consciously knew that my life had changed completely and in time came to realize and accept that my friends would grow distant and ultimately leave me behind. This sense of loneliness would become a significant ingredient in my art.
            <br /><br />
            My figurative paintings are part of a series that explores a sense of isolation and sterility, using bold and brilliant colors depicting city streets contrasted with the continuous use of the lone man.
            <br /><br />
            My abstract works utilize an organic approach and illustrate the molecular realm of the human body and the potential cohabitation of disease and infection. These meditations explore the theme of mortality intertwined with biology and confront the uncertainty and impermanence of life. 
            <br /><br />
            I rarely depict a direct allusion to my disability; however when I have, the style and intent of the artwork changes very little. I am very deliberate in the continuous use of the lone man. When I lost my leg, I consciously knew that my life had changed completely. I soon realized and accepted that my friends would grow distant and ultimately leave me behind.
            <br /><br />
            My intention is to convey a more subliminal approach to the content of the work and I do not directly address my feeling regarding being a cancer survivor. My abstract series best represents my feeling on surviving cancer. 
            <br /><br />
            Being handicapped is comparable to living in sustained crisis. After almost 35 years as an amputee, I eventually came to the realization that in spite of the inherent stress accompanying my disability, my response to challenging circumstances has been decidedly focused towards formulating solutions. My ability to ignore most everything else and concentrate solely on the immediacy of the situation has served as an effective coping mechanism. However, my single-minded approach ignored the inevitable anxieties resulting from such experiences. For me it seems a total miracle that I am a survivor, yet I have also seen friends and family not survive cancer and this fact has both troubled and humbled me. I often think about how things could have turned out and most every day I am reminded of my anomalous survival.
            <br /><br />
            The sentiments I instill into my art link directly to my subconscious and help establish a vital connection with my inner emotions. The end result is a deeper understanding of the path that has shaped my art.
